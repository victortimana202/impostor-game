// Función para obtener una palabra aleatoria de una categoría específica
export function getRandomWord(category = 'all') {
  let words = [];
  
  if (category === 'all') {
    // Combinar todas las categorías
    words = [
      ...colombianWords.animales,
      ...colombianWords.frutas,
      ...colombianWords.peliculas,
      ...colombianWords.cultura,
      ...colombianWords.lugares,
    ];
  } else if (colombianWords[category]) {
    words = colombianWords[category];
  } else {
    // Si la categoría no existe, usar todas
    words = [
      ...colombianWords.animales,
      ...colombianWords.frutas,
      ...colombianWords.peliculas,
      ...colombianWords.cultura,
      ...colombianWords.lugares,
    ];
  }
  
  const randomIndex = Math.floor(Math.random() * words.length);
  const selectedWord = words[randomIndex];
  
  // Agregar hint (primera letra)
  return {
    ...selectedWord,
    impostorHint: `Empieza con "${selectedWord.word.charAt(0).toUpperCase()}"`,
  };
}

